public class Variables {
    public static void main(String[] args) {
        int p = 1;
        int e = 2;
        int n = 3;
        double i = 4.0;
        float s = 5.0f;

        System.out.println("p: " + Integer.toString(p));
        System.out.println("e: " + Integer.toString(e));
        System.out.println("n: " + Integer.toString(n));
        System.out.println("i: " + Double.toString(i));
        System.out.println("s: " + Float.toString(s));
    }
    

}